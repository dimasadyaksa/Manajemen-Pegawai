    public String lastPayday(){
        String tgl = null;
        
        for(int i = 0;i<data.getListDirektur().size();i++){
            for(int j = 0;j<data.getListDirektur().get(i).getSlip().size();j++){
                tgl = data.getListDirektur().get(i).getSlip().get(j).getTanggal();
            }
        }
        
        for(int i = 0;i<data.getListManager().size();i++){
            for(int j = 0;j<data.getListManager().get(i).getSlip().size();j++){
                tgl = data.getListManager().get(i).getSlip().get(j).getTanggal();
            }
        }
        
        for(int i = 0;i<data.getListKaryawan().size();i++){
            for(int j = 0;j<data.getListKaryawan().get(i).getSlip().size();j++){
                tgl = data.getListKaryawan().get(i).getSlip().get(j).getTanggal();
            }
        }
        
        for(int i = 0;i<data.getListMagang().size();i++){
            for(int j = 0;j<data.getListMagang().get(i).getSlip().size();j++){
                tgl = data.getListMagang().get(i).getSlip().get(j).getTanggal();
            }
        }
        
        return tgl;
    }
    
    public int ekstrakBulan(String tgl){
        int bulan;
        
        bulan = Integer.parseInt(Character.toString(tgl.charAt(5))+Character.toString(tgl.charAt(6)));
        
        return bulan;
    }
    
    public int ekstrakTahun(String tgl){
        int bulan;
        
        bulan = Integer.parseInt(Character.toString(tgl.charAt(0))+Character.toString(tgl.charAt(1))+
                Character.toString(tgl.charAt(2))+Character.toString(tgl.charAt(3)));
        
        return bulan;
    }
    
    
    public String formatUang(double uang){
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();

        formatRp.setCurrencySymbol("Rp. ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');
        kursIndonesia.setDecimalFormatSymbols(formatRp);
        
        return kursIndonesia.format(uang); 
    }

    public void inisiasi(){
        
        pegawaiJumlah.setText(Integer.toString(stat.getJumlahDirektur()+
                stat.getJumlahKaryawan()+stat.getJumlahMagang()+stat.getJumlahManager()));
        direkturJumlah.setText(Integer.toString(stat.getJumlahDirektur()));
        managerJumlah.setText(Integer.toString(stat.getJumlahManager()));
        karyawanJumlah.setText(Integer.toString(stat.getJumlahKaryawan()));
        magangJumlah.setText(Integer.toString(stat.getJumlahMagang()));
       // loadChart();
        tc.tabelStatistik(tabelStatistik,data.getListDirektur(),data.getListManager(),
        data.getListKaryawan(),data.getListMagang());
        DateFormat dtform = new SimpleDateFormat("yyyy-MM-dd");
        Date dt = new Date();
//        dateToday.setFormats("dd-MMMM-yyyy");
//        dateToday.setDate(dt);
        feed.setWrapStyleWord(true);
        feed.setLineWrap(true);
        loadNews();
        if(lastPayday()== null){
            lastPayment = ekstrakBulan(dtform.format(dt));
            lastPaymentYear = ekstrakTahun(dtform.format(dt));
        }else{
            lastPayment = ekstrakBulan(lastPayday());
            lastPaymentYear = ekstrakTahun(lastPayday());
        }
        tc.tabelFeed(feedtab);
        newsDelButton();
        String date = dtform.format(dateToday.getDate());
        currMonth = ekstrakBulan(date);
        currYear = ekstrakTahun(date);
        if(currYear==lastPaymentYear){
           if(currMonth>lastPayment){
                pay.setEnabled(true);
           }
           
        }else if(currYear>lastPaymentYear){
            pay.setEnabled(true);
        }else{
            pay.setEnabled(false);

        }
        System.out.println(currYear+" "+lastPaymentYear);
//        loadChart();
    }
    
