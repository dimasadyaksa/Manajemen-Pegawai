    private void konfirmGajiDirekturActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_konfirmGajiDirekturActionPerformed
        // TODO add your handling code here:
        double tempGaji = direktur.getGajiBersih();
        double tempBonus = direktur.getBonus();
        
        double newGaji = tempGaji;
        double newBonus = tempBonus;
        
        int i = tabelDirektur.getSelectedRow();
        
        koneksi kon = new koneksi();
        
        if(i == -1){
            if(!newGajiDirektur.getText().equals("")){
                newGaji = Double.parseDouble(newGajiDirektur.getText());
            
            }
        
            if(!newbonusDirektur.getText().equals("")){
                newBonus = Double.parseDouble(newbonusDirektur.getText());
            }
            
            direktur.setGajiBersih(newGaji);
            direktur.setBonus(newBonus);
            if(newGaji > 0 && newBonus >= 0){
                
                for(int x = 0 ;x<data.getListDirektur().size();x++){
                    if(tabelDirektur.getValueAt(x,5).toString().contains("Belum")){

                    data.getListDirektur().get(x).setGaji(newGaji);
                    kon.exec("UPDATE `pegawai` SET `GAJI`= "+data.getListDirektur().get(x).getGaji()
                            +" WHERE NIP = "
                    + data.getListDirektur().get(x).getNIP());
                
                    }
                }
                
                stat.ubahGaji(newGaji, newBonus, 1);
            }else{
                JOptionPane.showMessageDialog(null,"Gaji tidak boleh 0\nBonus tidak boleh negatif!");  
            }
            newGajiDirektur.setText("");
            newbonusDirektur.setText("");
        }else{
            newGaji = data.getListDirektur().get(i).getGaji();
            if(!newGajiDirektur.getText().equals("")){
            newGaji = Double.parseDouble(newGajiDirektur.getText());
            
            }
        
            if(!newbonusDirektur.getText().equals("")){
                newBonus = Double.parseDouble(newbonusDirektur.getText());
            }
        
            if(newGaji > 0 && newBonus >= 0){
                kon = new koneksi();
                direktur.setBonus(newBonus);
                data.getListDirektur().get(i).setGaji(newGaji);
                kon.exec("UPDATE `pegawai` SET `GAJI`= "+data.getListDirektur().get(i).getGaji()+" WHERE NIP = "
                    + data.getListDirektur().get(i).getNIP());
                
                newGajiDirektur.setText("");
                newbonusDirektur.setText("");
            }
        }
        
        
        
        reloadDirektur();
        buttonControlDir();
        
    }//GEN-LAST:event_konfirmGajiDirekturActionPerformed

